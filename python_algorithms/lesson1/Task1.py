# 1. Выполнить логические побитовые операции «И», «ИЛИ» и др.
# над числами 5 и 6. Выполнить над числом 5 побитовый сдвиг вправо
# и влево на два знака.

n1 = 5
n2 = 6

bit_or = n1 | n2
bit_and = n1 & n2

bit_right = n1 >> 2
bit_left = n1 << 2

print(f"Побитовое «ИЛИ» 5 | 6 = {bit_or}")
print(f"Побитовое «И» 5 & 6 = {bit_and}")

print(f"Побитовый сдвиг вправо от 5 на 2 знака = {bit_right}")
print(f"Побитовый сдвиг влево от 5 на 2 знака = {bit_left}")
